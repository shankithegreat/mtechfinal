    public static String helperOne(String s) {
        if (s == null) return "";
        StringBuilder sb = new StringBuilder();
        for (int i=0;i<s.length();i++) {
            char c = s.charAt(i);
            if (Character.isLetterOrDigit(c)) sb.append(c);
            else sb.append('_');
        }
        return sb.toString();
    }

